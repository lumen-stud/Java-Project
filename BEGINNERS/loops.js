// LOOPS

for (let a = 1; a <= 10; a++) {
  //'for'loop
  console.log(4 + a);
}

let b = 1;
while (b < 5) {
  console.log("literation number" + b); //'while' loop
  b++;
}

let c = 20;
do {
  console.log(15 + c); //'Do while' loop
  c++;
} while (c <= 20);

const EvenNum = [2, 4, 6, 8, 10, 12]; //'For Loop'
for (const num of EvenNum) {
  console.log("Eli" + num);
}

// LOOP END
